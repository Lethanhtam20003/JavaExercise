package Ex_4_1_BankAccount;

public class Date {
	private int day;
	private int morth;
	private int year;
	/**
		new Date(12, 23, 2021);
	 * @param day
	 * @param morth
	 * @param year
	 */
	public Date(int day, int morth, int year) {
		this.day = day;
		this.morth = morth;
		this.year = year;
	}	
}
